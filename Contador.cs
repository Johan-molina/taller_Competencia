using System;

public class Class1
{
    public Class1()
    {
        private int contador;
    

        public Contador()
        {
            this.contador = 0;
        }

        public int incrementar()
        {
            contador++;
            return contador;
        }

        public int decrementar()
        {
            contador--;
            return contador;
        }
    }
}
}
